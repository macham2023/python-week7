#The extend function in python : 
# extend(): It adds the specified list elements (or any iterable) to the end of the current list.


car_names = ["Benz", "Lamborghini", "Lexus", "Camry", "Tesla"]

country_names = ["Brazil", "Nigeria", "Spain", "England", "Cameroon"]

car_names.extend(country_names)

print(car_names)
